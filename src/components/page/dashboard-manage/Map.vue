<script>
import anime from "animejs";
import data from "./data.js";
var timer;
export default {
  data() {
    return {
      totalData: data,
      timer: null,

    };
  },
  computed: {
    showData() {
      return this.totalData[0];
    }
  },
  methods: {
    animate() {
      let timeline = anime.timeline({
        loop: false,
        direction: "normal",
        easing: "easeInOutQuad",
        autoplay: true
      });
      timeline.add({
        targets: ".line",
        opacity: [0, 1],
        strokeDashoffset: [anime.setDashoffset, 0],
        duration: 500
      });
      // 获取内容高度，动画显示到该内容高度
      console.log(document.querySelector(".content_info").clientHeight);
      let contentHeight =
        document.querySelector(".content_info").clientHeight || 144;
      timeline.add({
        targets: ".content_info",
        height: ["0", contentHeight + "px"],
        duration: 500
      });
      // anime({
      //     targets: '.line',
      //     loop: false,
      //     duration: 500,
      //     easing: 'easeInOutQuad',
      //     strokeDashoffset: [anime.setDashoffset, 0],
      //     direction: 'normal'
      // })
    },
    // 修改totalData的顺序，改变显示效果
    changeData() {
      if (this.timer) clearInterval(this.timer);
      this.timer = setInterval(() => {
        this.animate();
        this.totalData.push(this.totalData.shift());
      }, 3000);
      this.$once("hook:beforeDestroy", () => {
        clearInterval(this.timer);
      });
    },
    clearTimer(){
      clearInterval(this.timer);
    }
  },
  created() {
    this.$nextTick(() => {
      anime({
        targets: ".anime",
        loop: true,
        opacity: [0, 1],
        duration: 500,
        easing: "easeInOutQuad",
        direction: "alternate"
      });
      this.animate();
      this.changeData();
    });
  },
